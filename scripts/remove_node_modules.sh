#!/usr/bin/env bash

# Cache where the script is getting called from to return there once done.
original_directory=$(pwd)

# Change directory to scripts dir.
cd "$(dirname "$0")" || exit 1

# Move to root
cd ../

# Remove all node_modules.
# TODO: Loop through packages instead of hard code.
rm -rf node_modules
rm -rf ui/app/node_modules
rm -rf ui/guest/node_modules
rm -rf ui/legacy/node_modules
rm -rf ui/scss/node_modules

# Return to whatever directory this may have been called from.
cd "$original_directory" || exit 1
